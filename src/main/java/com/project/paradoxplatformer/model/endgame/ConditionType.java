package com.project.paradoxplatformer.model.endgame;

/**
 * Enum representing the types of conditions that can occur in the game.
 * These conditions determine the outcome of the game, such as winning or
 * losing.
 */
public enum ConditionType {

    /**
     * Represents a winning condition in the game.
     */
    WIN,

    /**
     * Represents a losing condition in the game.
     */
    LOSE
}
