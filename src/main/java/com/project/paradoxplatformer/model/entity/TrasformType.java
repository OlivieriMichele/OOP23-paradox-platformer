package com.project.paradoxplatformer.model.entity;

/**
 * Defines the types of transformations that can be applied to an entity.
 * <p>
 * This enumeration provides different ways to alter the properties of an
 * entity.
 * </p>
 */
public enum TrasformType {
    /**
     * Represents a transformation that changes the position of the entity.
     */
    DISPLACEMENT,

    /**
     * Represents a transformation that changes the height of the entity.
     */
    HEIGHT,

    /**
     * Represents a transformation that changes the width of the entity.
     */
    WIDTH
}
