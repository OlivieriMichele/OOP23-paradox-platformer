package com.project.paradoxplatformer.view.javafx;

/**
 * Enum representing different pages or views in the JavaFX application.
 * Each enum constant corresponds to a specific page or view.
 */
public enum PageIdentifier {
    /**
     * Represents the main menu page.
     */
    MENU,

    /**
     * Represents the game play page.
     */
    GAME,

    /**
     * Represents the settings page.
     */
    SETTINGS,

    /**
     * Represents the pause page or screen.
     */
    PAUSE,

    /**
     * A placeholder value used when no specific page is applicable.
     */
    EMPTY
}
